package MODEL;

public class WibuBean{
	int id_wibu;
	String nama_wibu, nama_asli, email, username, status, password;

    public int getId_wibu() {
        return id_wibu;
    }

    public void setId_wibu(int id_wibu) {
        this.id_wibu = id_wibu;
    }

    public String getNama_wibu() {
        return nama_wibu;
    }

    public void setNama_wibu(String nama_wibu) {
        this.nama_wibu = nama_wibu;
    }

    public String getNama_asli() {
        return nama_asli;
    }

    public void setNama_asli(String nama_asli) {
        this.nama_asli = nama_asli;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
    